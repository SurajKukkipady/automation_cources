// Relational or comparison operators

// > - more than
// < - less than
// >= - more than or equal to
// <= - less than or equal to

var result = 10 > 5 
console.log(result); // true

// equalit operators

var x = 1
console.log(x == '1'); // lose comparison, data types are not considered
console.log(x === '1'); // strict comparison, data types are considered
console.log(x === 1); // true, both value and type are the same